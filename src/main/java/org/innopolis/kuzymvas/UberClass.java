package org.innopolis.kuzymvas;

/**
 * Класс, использующий особенности обработки Java переменных в finally
 * для генерации максимально возможного байткода по отношению к объему исходного кода.
 * Не имеет практического смысла, используется как ресурс для заполнения Metasoace мусором
 */
public class UberClass {
    {
        int a;
        try {
            a = 0;
        } finally {
            try {
                a = 0;
            } finally {
                try {
                    a = 0;
                } finally {
                    try {
                        a = 0;
                    } finally {
                        try {
                            a = 0;
                        } finally {
                            try {
                                a = 0;
                            } finally {
                                try {
                                    a = 0;
                                } finally {
                                    try {
                                        a = 0;
                                    } finally {
                                        try {
                                            a = 0;
                                        } finally {
                                            try {
                                                a = 0;
                                            } finally {
                                                try {
                                                    a = 0;
                                                } finally {
                                                    try {
                                                        a = 0;
                                                    } finally {
                                                        a = 0;
                                                    }
                                                }
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }
}

